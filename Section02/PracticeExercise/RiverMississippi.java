package Section02.PracticeExercise;

public class RiverMississippi {

    public static void main(String[] args) {

        String river = "Mississippi";
        System.out.println(river);
        System.out.println(river.length());

        String river2 = river.replace("i", "ii");
        System.out.println(river2);
        System.out.println(river2.length());

        String river3 = river2.replace("ss", "s");
        System.out.println(river3);
        System.out.println(river3.length());

    }

}
